import math


def sqrt(num):
    return math.sqrt(num)


def cosine(num):
    return math.cos(num)


def log10(num):
    return math.log10(num)


def factorial(num):
    return math.factorial(num)


print(f"Square root of 9 is {sqrt(9)}")
print(f"cos(9) is {cosine(9)}")
print(f"log(10) with base 10 is {log10(10)}")
print(f"10! is {factorial(10)}")

